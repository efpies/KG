#pragma hdrstop

#include "Vertice.h"
//---------------------------------------------------------------------------
#pragma package(smart_init)
//---------------------------------------------------------------------------
// Lifecycle
//---------------------------------------------------------------------------
Vertice::Vertice(const double x, const double y, const double z, const UnicodeString& _tag)
	: tag(_tag)
{
	coords = new Matrix(1, 4);
	setX(x);
	setY(y);
	setZ(z);
	setAug(1);
}
//---------------------------------------------------------------------------
Vertice::Vertice(const Vertice& src)
	: tag(src.tag)
{
	coords = new Matrix(*src.coords);
}
//---------------------------------------------------------------------------
Vertice::~Vertice()
{
	delete coords;
}
